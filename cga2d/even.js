export class CGAEven {
  /**
   * Constructor
   * @param {number} s
   * @param {number} xy
   * @param {number} xp
   * @param {number} xn
   * @param {number} yp
   * @param {number} yn
   * @param {number} pn
   * @param {number} xypn
   */
  constructor(s, xy, xp, xn, yp, yn, pn, xypn) {
    this.s = s;
    this.xy = xy;
    this.xp = xp;
    this.xn = xn;
    this.yp = yp;
    this.yn = yn;
    this.pn = pn;
    this.xypn = xypn;
  }
}
