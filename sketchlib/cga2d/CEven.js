export class CEven {
  /**
   * Constructor
   * @param {number} scalar
   * @param {number} xy
   * @param {number} xp
   * @param {number} xm
   * @param {number} yp
   * @param {number} ym
   * @param {number} pm
   * @param {number} xypm
   */
  constructor(scalar, xy, xp, xm, yp, ym, pm, xypm) {
    this.scalar = scalar;
    this.xy = xy;
    this.xp = xp;
    this.xm = xm;
    this.yp = yp;
    this.ym = ym;
    this.pm = pm;
    this.xypm = xypm;
  }
}
