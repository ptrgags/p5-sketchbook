export class COdd {
  /**
   * Constructor
   * @param {number} x
   * @param {number} y
   * @param {number} p
   * @param {number} m
   * @param {number} xyp
   * @param {number} xym
   * @param {number} xpm
   * @param {number} ypm
   */
  constructor(x, y, p, m, xyp, xym, xpm, ypm) {
    this.x = x;
    this.y = y;
    this.p = p;
    this.m = m;
    this.xyp = xyp;
    this.xym = xym;
    this.xpm = xpm;
    this.ypm = ypm;
  }
}
